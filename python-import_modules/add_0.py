"""
Defines a function `add(a, b)` that returns the sum of two integers.
Used in 0-add.py to demonstrate function import.
"""

def add(a, b):
    """My addition function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a + b
    """
    return a + b
